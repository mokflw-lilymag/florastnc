-- ============================================================
-- FloraSync FAQ 봇 테이블 + 초기 데이터
-- Supabase SQL Editor에서 실행
-- ============================================================

CREATE TABLE IF NOT EXISTS support_faq (
    id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
    category TEXT NOT NULL,          -- 카테고리명
    category_icon TEXT NOT NULL,     -- 이모지 아이콘
    category_order INT DEFAULT 0,    -- 카테고리 정렬 순서
    question TEXT NOT NULL,          -- 질문
    answer TEXT NOT NULL,            -- 답변 (마크다운 가능)
    question_order INT DEFAULT 0,    -- 질문 정렬 순서
    is_featured BOOLEAN DEFAULT false, -- 입장 시 바로 보이는 주요 질문
    is_active BOOLEAN DEFAULT true,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    updated_at TIMESTAMPTZ DEFAULT NOW()
);

-- RLS 허용 (로그인 사용자 읽기)
ALTER TABLE support_faq ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Authenticated users can read FAQ" ON support_faq
    FOR SELECT USING (auth.role() = 'authenticated');
CREATE POLICY "Super admin can manage FAQ" ON support_faq
    FOR ALL USING (true);

